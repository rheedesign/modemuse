function summarizeAdminUsageRows(rows, source = "remote") {
  const featureMap = new Map();
  const userMap = new Map();
  const uniqueUsers = new Set();
  const normalizedRows = Array.isArray(rows) ? rows : [];

  for (const row of normalizedRows) {
    const featureKey = row.feature || "unknown";
    const featureEntry = featureMap.get(featureKey) || {
      feature: featureKey,
      totalCount: 0,
      successCount: 0,
      errorCount: 0,
    };
    featureEntry.totalCount += 1;
    featureEntry.successCount += row.success ? 1 : 0;
    featureEntry.errorCount += row.success ? 0 : 1;
    featureMap.set(featureKey, featureEntry);

    const userKey = row.user_id || row.user_email || "unknown";
    const userEntry = userMap.get(userKey) || {
      userId: row.user_id || "",
      userEmail: row.user_email || "",
      totalCount: 0,
      successCount: 0,
      errorCount: 0,
    };
    userEntry.totalCount += 1;
    userEntry.successCount += row.success ? 1 : 0;
    userEntry.errorCount += row.success ? 0 : 1;
    userMap.set(userKey, userEntry);

    if (row.user_id) uniqueUsers.add(row.user_id);
    else if (row.user_email) uniqueUsers.add(row.user_email);
  }

  const featureBreakdown = Array.from(featureMap.values()).sort((a, b) => {
    if (b.totalCount !== a.totalCount) return b.totalCount - a.totalCount;
    return a.feature.localeCompare(b.feature);
  });
  const userBreakdown = Array.from(userMap.values()).sort((a, b) => {
    if (b.totalCount !== a.totalCount) return b.totalCount - a.totalCount;
    const aKey = a.userEmail || a.userId || "";
    const bKey = b.userEmail || b.userId || "";
    return aKey.localeCompare(bKey);
  }).slice(0, 25);

  const startOfDay = new Date();
  startOfDay.setHours(0, 0, 0, 0);
  const dailyCount = normalizedRows.filter((row) => new Date(row.created_at || 0) >= startOfDay).length;
  const errorCount = normalizedRows.filter((row) => !row.success).length;

  return {
    source,
    stats: {
      dailyCount,
      totalCount: normalizedRows.length,
      errorCount,
      userCount: uniqueUsers.size,
    },
    featureBreakdown,
    userBreakdown,
    recentEvents: normalizedRows.slice(0, 25),
  };
}

async function getAiUsageStats(userId) {
  const startOfDay = new Date();
  startOfDay.setHours(0, 0, 0, 0);
  const startIso = startOfDay.toISOString();

  try {
    const [{ count: dailyCount, error: dailyError }, { count: totalCount, error: totalError }] = await Promise.all([
      supabase
        .from(AI_USAGE_TABLE)
        .select("id", { count: "exact", head: true })
        .eq("user_id", userId)
        .gte("created_at", startIso),
      supabase
        .from(AI_USAGE_TABLE)
        .select("id", { count: "exact", head: true })
        .eq("user_id", userId),
    ]);
    if (dailyError) throw dailyError;
    if (totalError) throw totalError;
    return { dailyCount: dailyCount || 0, totalCount: totalCount || 0, source: "supabase" };
  } catch {
    const events = readLocalAiUsageEvents(userId);
    const dailyCount = events.filter((event) => new Date(event.created_at || 0) >= startOfDay).length;
    return { dailyCount, totalCount: events.length, source: "local" };
  }
}

async function recordAiUsageEvent({ userId, userEmail, feature, success, errorMessage = "", metadata = {} }) {
  const event = {
    user_id: userId,
    user_email: userEmail || null,
    feature,
    success,
    error_message: errorMessage || null,
    metadata,
    created_at: new Date().toISOString(),
  };

  try {
    const { error } = await supabase.from(AI_USAGE_TABLE).insert(event);
    if (error) throw error;
  } catch {
    writeLocalAiUsageEvent(userId, event);
  }

  return event;
}

async function ensureAiUsageAvailable(userId) {
  const stats = await getAiUsageStats(userId);
  if (stats.dailyCount >= AI_USAGE_DAILY_LIMIT) {
    return {
      allowed: false,
      message: `You’ve reached the daily AI cap (${AI_USAGE_DAILY_LIMIT} requests/day).`,
      stats,
    };
  }
  if (stats.totalCount >= AI_USAGE_TOTAL_LIMIT) {
    return {
      allowed: false,
      message: `You’ve used all ${AI_USAGE_TOTAL_LIMIT} free AI requests.`,
      stats,
    };
  }
  return { allowed: true, stats };
}

async function runTrackedAnthropicRequest({
  user,
  feature,
  requestBody,
  metadata = {},
}) {
  if (!user?.id) throw new Error("You need to be signed in.");
  if (!AI_ENABLED) throw new Error(AI_PAUSED_MESSAGE);

  const quota = await ensureAiUsageAvailable(user.id);
  if (!quota.allowed) throw new Error(quota.message);

  let response;
  let data;
  try {
    response = await fetch("/api/anthropic/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": import.meta.env.VITE_ANTHROPIC_API_KEY,
        "anthropic-version": "2023-06-01",
        "anthropic-dangerous-direct-browser-access": "true",
      },
      body: JSON.stringify(requestBody),
    });
    data = await response.json();
  } catch (err) {
    await recordAiUsageEvent({
      userId: user.id,
      userEmail: user.email,
      feature,
      success: false,
      errorMessage: err.message || "Network error",
      metadata,
    });
    throw err;
  }

  if (!response.ok) {
    const errorMessage = data?.error?.message || `API error: ${response.status}`;
    await recordAiUsageEvent({
      userId: user.id,
      userEmail: user.email,
      feature,
      success: false,
      errorMessage,
      metadata,
    });
    throw new Error(errorMessage);
  }

  await recordAiUsageEvent({
    userId: user.id,
    userEmail: user.email,
    feature,
    success: true,
    metadata,
  });

  return data;
}

