function StyleInspoPicker({ value, onChange, max = 3, compact = false, title = "Style Inspo", subtitle = "Pick up to 3" }) {
  const selected = normalizeStyleInspoSelection(value);

  function toggle(option) {
    const exists = selected.includes(option);
    let next;
    if (exists) {
      next = selected.filter((item) => item !== option);
    } else if (selected.length < max) {
      next = [...selected, option];
    } else {
      next = [...selected.slice(1), option];
    }
    onChange(next);
  }

  return (
    <div>
      <span className="mb-2 block text-sm font-medium text-gray-700">{title}</span>
      <p style={{ margin: "0 0 10px", fontSize: compact ? "12px" : "13px", color: "#6b7280" }}>{subtitle}</p>
      <div style={{ display: "flex", flexWrap: "wrap", gap: "8px" }}>
        {STYLE_INSPO_OPTIONS.map((opt) => {
          const active = selected.includes(opt);
          return (
            <button
              key={opt}
              type="button"
              onClick={() => toggle(opt)}
              style={{
                padding: "10px 14px",
                borderRadius: "999px",
                border: active ? "1.5px solid #B08A4A" : "1px solid #e0e0e0",
                background: active ? "#F5EDE0" : "white",
                color: active ? "#B08A4A" : "#555",
                fontSize: compact ? "12px" : "13px",
                fontWeight: active ? 700 : 500,
                cursor: "pointer",
                transition: "all 0.15s ease",
              }}
            >
              {opt}
            </button>
          );
        })}
      </div>
    </div>
  );
}

