function getAiUsageStorageKey(userId) {
  return `${AI_USAGE_STORAGE_KEY}:${userId}`;
}

function readLocalAiUsageEvents(userId) {
  try {
    const raw = localStorage.getItem(getAiUsageStorageKey(userId));
    const parsed = raw ? JSON.parse(raw) : [];
    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

function writeLocalAiUsageEvent(userId, event) {
  const events = readLocalAiUsageEvents(userId);
  events.push(event);
  localStorage.setItem(getAiUsageStorageKey(userId), JSON.stringify(events.slice(-300)));
}

function readAllLocalAiUsageEvents() {
  try {
    if (typeof localStorage === "undefined") return [];
    const events = [];
    for (let i = 0; i < localStorage.length; i += 1) {
      const key = localStorage.key(i);
      if (!key || !key.startsWith(`${AI_USAGE_STORAGE_KEY}:`)) continue;
      const raw = localStorage.getItem(key);
      const parsed = raw ? JSON.parse(raw) : [];
      if (Array.isArray(parsed)) {
        events.push(...parsed);
      }
    }
    return events;
  } catch {
    return [];
  }
}

